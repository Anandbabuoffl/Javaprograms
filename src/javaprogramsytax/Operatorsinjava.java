package javaprogramsytax;

public class Operatorsinjava {//operators

	public static void main(String[] args) {
		
          int a=10;
          int b=30;
          
         
          System.out.println(a+b);
          System.out.println(a-b);
          System.out.println(a*b);
          System.out.println(a/b);
          System.out.println(a%b);//Arithematic operators
          System.out.println();
          
          int c=10;
          double d=20.5;
          
         System.out.println(c<d);
         System.out.println(c>d);
         System.out.println(c<=d);
         System.out.println(c>=d);
         System.out.println(c!=d);
         System.out.println(c==d);//relation operators it returns boolean values
         
         System.out.println();
         
         boolean e=true;
         boolean f=false;
         
         System.out.println(e&&f);
         System.out.println(e||f);
         System.out.println(!e);
         System.out.println(!f);//logical operators it only deals with boolean values
         
         System.out.println();
         
         int g=30;
         int h;
         System.out.println(h=g);
         System.out.println(g++);
         System.out.println(g+=3);
         System.out.println(h*=2);
         System.out.println(g/=2);//Assignment operators
         
         System.out.println();
         
         int i=100;
         
         System.out.println(++i);
         System.out.println(--i);
         System.out.println(i++);
         System.out.println(i--);
         System.out.println(i);//increment/decrement operators
         System.out.println();
         
         
         
         
          
          
          
	}

}
